    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        StringBuilder errors = new StringBuilder();
        if (txtIdAnggota.getText().trim().isEmpty()) {
            errors.append("- ID Anggota belum diisi.\n");
        }

        if (txtIdBuku.getText().trim().isEmpty()) {
            errors.append("- ID Buku belum diisi.\n");
        }
        
        Peminjaman pmj = new Peminjaman();
        
        if (errors.length() > 0) {
            JOptionPane.showMessageDialog(null, 
                "Mohon lengkapi data berikut:\n" + errors.toString(),
                "Input Tidak Lengkap",
                JOptionPane.INFORMATION_MESSAGE);
        } else {
            pmj.setIdpeminjaman(Integer.parseInt(txtIdPeminjaman.getText()));
            pmj.getAnggota().setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
            pmj.getBuku().setIdbuku(Integer.parseInt(txtIdBuku.getText()));
            pmj.setPegawai((Pegawai) cmbPegawai.getSelectedItem());
            pmj.setTanggalpinjam(txtTanggalPinjam.getText());
            pmj.save();
            txtIdPeminjaman.setText(Integer.toString(pmj.getIdpeminjaman()));
            tampilData();

            JOptionPane.showMessageDialog(null, 
                "Data berhasil disimpan!", 
                "Sukses", 
                JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblPeminjaman.getModel();
        int row = tblPeminjaman.getSelectedRow();
        
        Peminjaman pmj = new Peminjaman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        pmj.delete();
        kosongkanForm();
        tampilData();
        
    }//GEN-LAST:event_btnhapusActionPerformed

