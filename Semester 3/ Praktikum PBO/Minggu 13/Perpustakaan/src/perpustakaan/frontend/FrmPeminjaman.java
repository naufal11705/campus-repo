    public void tampilData(){
        String[] kolom = {"ID" , "Peminjam" , "Judul" , "Pegawai" , "Tanggal Pinjam", "Tanggal Kembali"};
        ArrayList<Peminjaman> list = new Peminjaman().getAll();
        Object rowData[] =new Object[6];
        
        tblPeminjaman.setModel(new DefaultTableModel(new Object[][] {},kolom));
        
        for(Peminjaman pmj : list){
            rowData[0] = pmj.getIdpeminjaman();
            rowData[1] = pmj.getAnggota().getNama();
            rowData[2] = pmj.getBuku().getJudul();
            rowData[3] = pmj.getPegawai().getNama();
            rowData[4] = pmj.getTanggalpinjam();
            rowData[5] = pmj.getTanggalkembali();
            
            ((DefaultTableModel)tblPeminjaman.getModel()).addRow(rowData);
        }
    }
    
    public void tampilkanCmbPegawai() {
        cmbPegawai.setModel(new DefaultComboBoxModel(new Pegawai().getAll().toArray()));
    }    
    
    public void cari(String keyword){
        String[] kolom = {"ID" , "Kategori" , "Judul" , "Penulis", "Penerbit"};
        ArrayList<Buku> list = new Buku().search(keyword);
        Object rowData[] =new Object[5];
        
        tblPeminjaman.setModel(new DefaultTableModel(new Object[][] {},kolom));
        for(Buku buku : list){
            rowData[0] = buku.getIdbuku();
            rowData[1] = buku.getKategori().getNama();
            rowData[2] = buku.getJudul();
            rowData[3] = buku.getPenulis();
            rowData[4] = buku.getPenerbit();
            
            ((DefaultTableModel)tblPeminjaman.getModel()).addRow(rowData);
        }
    }
