    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id = Integer.parseInt(txtIdBuku.getText());
        
        Buku buku = new Buku();
        boolean isAvailable = buku.cekIdBuku(txtIdBuku.getText());
        
        if (isAvailable) {
            JOptionPane.showMessageDialog(this, "Buku tidak ditemukan", "Warning", JOptionPane.WARNING_MESSAGE);
            txtIdBuku.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Buku ditemukan", "Information", JOptionPane.INFORMATION_MESSAGE);
            labelBuku.setText(buku.getById(id).getJudul());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

