    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblPeminjaman.getModel();
        int row = tblPeminjaman.getSelectedRow();
        Peminjaman pmj = new Peminjaman();
        
        int id = Integer.parseInt(txtIdPeminjaman.getText());
        
        txtIdAnggota.setEnabled(false);
        txtIdBuku.setEnabled(false);
        txtTanggalPinjam.setEnabled(false);
        btnSimpan.setEnabled(false);
        btnUpdate.setEnabled(true);
        txtTanggalKembali.setEnabled(true);
        
        txtIdAnggota.setText(String.valueOf(pmj.getById(id).getAnggota().getIdanggota()));
        txtIdBuku.setText(String.valueOf(pmj.getById(id).getBuku().getIdbuku()));
        labelAnggota.setText((String)pmj.getById(id).getAnggota().getNama());
        labelBuku.setText((String)pmj.getById(id).getBuku().getJudul());
        
        pmj = pmj.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdPeminjaman.setText(model.getValueAt(row, 0).toString());
        cmbPegawai.getModel().setSelectedItem(pmj.getPegawai());
        txtTanggalPinjam.setText(model.getValueAt(row, 4).toString());
        
        if (model.getValueAt(row, 5) == null) {
            txtTanggalKembali.setText("");
        } else {
            txtTanggalKembali.setText(model.getValueAt(row, 5).toString());
        }
    }//GEN-LAST:event_tblPeminjamanMouseClicked

